                            <h4><strong>Ways to do product hunting:</strong></h4>
                            <ul>
                                <li>· Use minus string method, for instance, type –(any random value) ,-ihbdew2,
                                    and then search.</li>
                                <li>· Use the generic word in the merchant word, black box, amazon search bar,
                                    and then extract some two-tail keyword.</li>
                                <li>· Use coupon websites.</li>
                                <li>· See the stores of Amazon sellers.</li>
                                <li>· See for amazon best sellers.</li>
                                <li>· Alibaba</li>
                                <li>· AliExpress</li>
                                <li>· Pinterest or other social media sites.</li>
                            </ul>
                            <h4><strong>Criteria for Product hunting for US market 2021:</strong></h4>
                            <ul>
                                <li>· At least 25 % of profit margin or a price greater than $ 20. (Ideally
                                    $20-$50).</li>
                                <li>· Merchant Word search volume 2 to 3 combined must be at least 100k; 25 k
                                    for a bulky product.</li>
                                <li>· Review rating must be at least 4 of 70 % of the sellers.</li>
                                <li>· Lower review the better usually we target reviews less than 150.</li>
                                <li>· No Fragile item</li>
                                <li>· Maximum 4 variations are allowed.</li>
                                <li>· No brand Dominance and at least two sellers must be Non-Amz.</li>
                                <li>· Helium 10 search volume must be at least 10k and competing for product on
                                    one keyword must not be more than 30000 the lesser the better.</li>
                                <li>· In most cases, eatable items are not allowed but if you have certification
                                    and experience of several launches then it may be considered.</li>
                                <li>· Electrical items must have the required certification and must be
                                    inspected.</li>
                                <li>· Check relevant keywords. You must search for each keyword in the amazon
                                    search bar, and ensure that at least 70 % of the same product are shown.
                                </li>
                                <li>· Use two tail and three tail keywords.</li>
                            </ul>
                            <p><strong>Criteria for Product hunting for UK market 2021:</strong></p>
                            <ul>
                                <li>Minimum price 20 GBP (Ideally 20 GBP to 50 GBP)</li>
                                <li>Main keyword search volume for merchant word must at least 50k.</li>
                                <li>50 % Of sellers must be earning 2000 GBP minimum.</li>
                                <li>Helium 10 search volume for 3 relevant keywords combine must be at least
                                    10,000.</li>
                                <li>Use two tail and three tail keywords.</li>
                                <li>Maximum 4 variations are allowed.</li>
                                <li>No brand Dominance and at least two sellers must be Non-Amz.</li>
                                <li>Review rating must be at least 4 of 70 % of the sellers.</li>
                                <li>Lower review the better usually we target reviews less than 150.</li>
                                <li>No fragile item</li>
                            </ul>
                            <p><strong>Patent Checking:</strong></p>
                            <ul>
                                <li>After the product approval, the patent of a product is checked. Website to
                                    check patent:</li>
                                <li>Google Patents
                                    <ul>
                                        <li>· https://worldwide.espacenet.com/patent/.</li>
                                        <li>· https://www.uspto.gov/.</li>
                                        <li>· https://www.dpma.de/.</li>
                                        <li>· https://www.tmdn.org/.</li>
                                        <li>· https://www.wipo.int/portal/en/index.html.</li>
                                    </ul>
                                </li>
                                <li>You can also hire freelancers from Fiverr or Upwork to check patent and must
                                    not use more than $ 100 for patent checking. Claims of patents must be read
                                    thoroughly. If you see only very few suppliers of a certain product then it
                                    may indicate it is patented.</li>
                            </ul>
                            <h4><strong>The patent can be divided into two types:</strong></h4>
                            <ul>
                                <li><strong>Utility Patent:</strong> A utility patent is a patent that covers
                                    the creation of a new or improved—and useful—product, process, or machine.
                                    We do not go for products having utility patents.</li>
                            </ul>
                            <p><strong>Design Patent:</strong> A design patent is a form of the legal protection
                                of the unique visual qualities of a manufactured item. There is a chance of
                                going into those products but you have to change the design by 40 %.</p>
                            <h4><strong>Sourcing and Logistics</strong></h4>
                            <ul>
                                <li>Buy UPC from an authentic website, usually, a speedy barcode is used or get
                                    UPC exemption to save money.</li>
                                <li>Make dummy Listing and open case from Amazon, three-time in different times
                                    to inquire whether my product is gated or not. You can use the image of the
                                    supplier and simple content will suffice. After successful product hunting
                                    and patent checking, we move towards sourcing our products. We also see the
                                    bad reviews of the product and especially of our competitor so that later on
                                    we can convey improvements to our seller. Usually, we use China for sourcing
                                    because it is cheaper. First, take rough estimates from suppliers about your
                                    product, you can use RFQ, if your margin comes up around 25 % then proceed,
                                    otherwise search for other countries or leave the product. Following are the
                                    platforms that are available to us:</li>
                                <li>Alibaba</li>
                                <li>AliExpress</li>
                                <li>Made-In-China</li>
                                <li>Local Sourcing Agent<br>
                                    Criteria for Selecting Supplier from Alibaba</li>
                                <li>Ideally should be at least 4 years old seller.</li>
                                <li>Must have a trade agreement.</li>
                                <li>At least 3 gems.</li>
                                <li>Response rate &gt; 80 %.</li>
                            </ul>
                            <h4><strong>Finalizing seller and some tips:</strong>·</h4>
                            <ul>
                                <li>Shortlist at least 10 suppliers, fulfilling the criteria above.</li>

                                <li>Start negotiating with them.</li>
                                <li>Start with the less amount of product then gradually increase volume and see
                                    price.</li>
                                <li>When showing pictures to the supplier, remove the logo of the product from
                                    the picture.</li>
                                <li>Order samples from 2 or 3 best suppliers while simultaneously ordering your
                                    product from your competitor. Once you have the sample, critically analyze
                                    it, you can use a drop test or ask an expert about the product.</li>
                                <li>Compare the quality of products with the quality of competitors. Then
                                    finally select the competitor having a similar quality to the competitor and
                                    lower price.</li>
                                <li>Ensure that the packaging is done according to your competitor.</li>
                                <li>Place the order.</li>
                                <li>Use trade assurance or Paypal to make payments</li>
                                <li>Usually 30 % is given in advance and 70 % after production.</li>
                                <li>Contact freight forwarder according to criteria as given for the seller.
                                </li>
                                <li>You have to give 100 % upfront.</li>
                                <li>You can choose FOB, EXW, or DDP. However, it is a good practice to go for
                                    FOB so that the third part can be involved, and it is usually cheaper
                                    because the Chinese government may give some rebates over it.</li>
                                <li>Ensure that the master carton is of the exact size specified by the
                                    supplier, you can also take sign from the freight forwarder.</li>
                                <li>In the meantime, while an order is manufactured, start working on listing
                                    optimization.</li>
                                <li>You can also ask the supplier to show them the product during production and
                                    after production via video call or you can employ a third party for
                                    inspection. Furthermore, you can also ask the actual address of the industry
                                    and then tell him that my relative may visit there, just to have some impact
                                    on the mind of a supplier.</li>
                                <li>Plan for the inventory keeping in mind the giveaways.</li>
                                <li>Generate two labels: shipping labels and product labels.</li>
                                <li>Usually products weighing greater than 500 g are considered ocean products.
                                </li>
                            </ul>
                            <h4><strong>Listing optimization:</strong></h4>
                            <ul>
                                <li>While the manufacturing is taking place, start with the listing
                                    optimization.</li>
                                <li>Use 3d images with white background, infographics, and lifestyle images.
                                </li>
                                <li>Your product must cover at least 80 % of the image.</li>
                                <li>Hire professional freelancers for these services.</li>
                                <li>Ensure that all the main keywords are indexed in the listing. Hire some
                                    professional content writer for this purpose. You can also use scribble tool
                                    in helium to optimize the listing.</li>
                                <li>Do backend optimization of listing and use Browse Tree Guide (BTG) for
                                    selecting search word, platinum keyword, etc.</li>
                                <li>Check for the listing of main competitor and best seller on Amazon in this
                                    niche.</li>
                                <li>FbaBeginners has many steps to follow, it takes 2 to 4 months to follow all
                                    the steps and launch a great product.</li>
                            </ul>
