                            </ul>
                            <p>&nbsp;</p>
                            <blockquote>
                                <h2><strong>2. </strong><strong>FBA Wholesale:</strong><br>
                                    Sell products of brands with their permission. It can be started with $3000+
                                    budget. You will be needing a LLC, resale certificate from you state(US
                                    only), professional website and email, VAT number(UK only)</h2>
                            </blockquote>
                            <h4><strong>WHOLESALE PRODUCT RESEARCH CRITERIA:</strong></h4>
                            <ul>
                                <li>Recommended price to list a product on amazon is 10 to 15 to get well profit
                                    margin</li>
                                <li>Don’t go for a product which has only 1 FBA seller</li>
                                <li>Check sellers carefully</li>
                                <li>Amazon prefers FBA wholesalers then FBM</li>
                                <li>Amazon usually pay after every 14 days but for first time it may take 15 to
                                    20 days initially</li>
                                <li>Reviews/rating matters to get more sales or buy box. Reviews 15 plus and
                                    ratting 4 and above.</li>
                                <li>90 days or 180 days average BSR should preferable.</li>
                                <li>Amazon should not be selling or constantly out of stock</li>
                                <li>(Prime check) on left side to get to know about FBA sellers on products</li>
                                <li>Because fbm is not your competitors Buy box will be your ‘s when you are in
                                    stock</li>
                                <li>Fba seller 3 to 4 minimums</li>
                                <li>Amazon should not be in stock. (Keepa can provide how many days amazon were
                                    out of stock or in stock in last 90 days)</li>
                                <li>Rating should be 4 plus</li>
                                <li>50 or more reviews</li>
                                <li>BSR_ 1 to 60k Average BSR of last 90 days</li>
                                <li>Reviews not less than 50 to 100</li>
                                <li>New offer 5 to 25</li>
                                <li>Package weight. standard not more than 2 kg (recommended)</li>
                            </ul>
                            <h4><strong>PRODUCTS TO AVOID</strong></h4>
                            <ul>
                                <li>Easily breakable – made of glass or fragile nature</li>
                                <li>HAZMAT- that can catch fire easily and are dangerous to ship and store</li>
                                <li>Multiple variations – Hard to cope up in start</li>
                                <li>That differ in size, color and quality</li>
                                <li>It’s hard to guess which variation sells best.</li>
                                <li>Plugs into Electrical Sockets</li>
                                <li>Liability issues</li>
                                <li>Quality issues</li>
                            </ul>
