                            <h4><strong>HOW TO DO PRODUCT HUNTING?</strong></h4>
                            <ul>
                                <li>Finding a suitable product for selling on Amazon.</li>
                                <li><strong>Tools:</strong>
                                    <ul>
                                        <li><strong>· Merchant Word: </strong>Use for finding products and main
                                            keyword (broad) search volume.<br>
                                            <strong>· Helium 10: </strong>It is a suite and has many other tools
                                            incorporated in to it which are:
                                            <ul>
                                                <li>Black Box: Use for finding products and their monthly sale,
                                                    revenue, BSR, reviews, and price.</li>
                                                <li>Magnet: Finding High-Volume and Highly Relevant Keywords for
                                                    Amazon, CPR giveaways.</li>
                                                <li>Cerebra: Same as Magnet, however, Reverse ASIN technique is
                                                    used to get relevant keywords and CPR giveaways.</li>
                                                <li>Scribble: List optimizer tool.</li>
                                                <li>Frankenstein: It process keywords.</li>
                                                <li>Keyword tracker: It helps us to know the rank of all our
                                                    keywords along with the changes in ranking that are
                                                    occurring.</li>
                                                <li>Index checker: It checks which keywords are indexed by
                                                    Amazon.</li>
                                            </ul>
                                        </li>
                                        <li><strong>Jungle Scout:</strong> Use for checking price, rank, monthly
                                            sale, revenues, reviews, and opportunity scores.</li>
                                        <li><strong>· Viral launch:</strong> Same function as Jungle Scout,
                                            however, it also shows market trends and its own analysis (VL
                                            analysis) where it recommends if pursuing a certain product is a
