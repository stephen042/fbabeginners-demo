                            <h4>You are looking to start selling on Amazon and not sure where to start? This
                                post will be extensive to elaborate all the models on Amazon</h4>
                            <p><strong>Major Business Models In Amazon</strong></p>
                            <ol>
                                <li>ARBITRAGE</li>
                                <li>WHOLESALE</li>
                                <li>PRIVATE LABEL</li>
                            </ol>
                            <blockquote>
                                <h2><strong>1. </strong><strong>FBA Arbitrage:</strong><br>
                                    Buying discounted products from online or offline retailers and sell on
                                    Amazon for a profit. It can be started with $1500+ budget</h2>
                            </blockquote>
                            <h4><strong>PRODUCT RESEARCH CRITERIA:</strong></h4>
                            <p>This is our criteria for Arbitrage products we find for our clients</p>
                            <ul>
                                <li>Non hazmat</li>
                                <li>No approval needed on ASIN or Category</li>
                                <li>No bundling</li>
                                <li>10$+ net profit</li>
                                <li>Last 60 days price consistency. See charts as well</li>
                                <li>No oversized product</li>
                                <li>Small product</li>
                                <li>Non fragile</li>
                                <li>No liquids preferably</li>
                            </ul>
                            <p><strong>Tools</strong></p>
                            <ul>
                                <li>Tactical Arbitrage</li>
                                <li>Keepa</li>
                                <li>Deals and discount sites</li>
                                <li>Funnelguru</li>
                                <li>Helium10</li>
                            </ul>
                            <h4><strong>Recommended Categories:</strong></h4>
                            <p>Non gated products and categories</p>
                            <ul>
                                <li>· Arts, Craft, and Sewing.</li>
                                <li>· Baby</li>
